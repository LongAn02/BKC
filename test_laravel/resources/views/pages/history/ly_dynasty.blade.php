                Hoàng Long, sông Châu Giang là đi xuôi dòng, trên sông Đáy, sông Hồng, sông Tô Lịch là đi ngược dòng. Sở
                dĩ nhà Lý đi bằng đường sông chứ không đi bằng đường biển cũng là bảo đảm an toàn vì thuyền phải tải
