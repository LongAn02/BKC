                Đi tiếp đến Gián Khẩu thì rẽ vào sông Đáy. Từ sông Đáy lại rẽ vào sông Châu Giang. Đến Phủ Lý đoàn
                thuyền ngược sông Hồng, rồi vào sông Tô Lịch trước cửa thành Đại La.<br>

