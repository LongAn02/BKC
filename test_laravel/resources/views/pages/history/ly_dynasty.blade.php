                vượng lâu dài, mở ra thời kỳ phục hưng toàn diện của đất nước.<br>

                <b>Dời đô về Đại La</b> <br>
                Gần 1 năm sau khi lên ngôi vua, tháng 7 âm lịch năm 1010, Lý Thái Tổ tiến hành dời đô từ Hoa Lư (Ninh
                Bình) về Đại La (Hà Nội). Ông đã ban hành Chiếu dời đô vào mùa xuân năm 1010.<br>

                Việc tìm đất, nghị bàn đến việc chuẩn bị để dời đô diễn ra tương đối khẩn trương. Từ Hoa Lư về thành Đại
                La có thể đi theo đường bộ hoặc theo đường thủy. Sử cũ không ghi chép chi tiết nhà Lý dời đô bằng đường
                nào. Các nhà nghiên cứu đã kết luận: triều nhà Lý dời đô bằng đường thủy, và chỉ có dời đô bằng đường
                thủy thì mới an toàn và tải được cả bộ máy triều đình đông đảo cùng vật chất bảo đảm đồ sộ đi
                kèm.<br>


                Bia Lý Thái Tổ bên sông Sào Khê tại Cố đô Hoa Lư, nơi vua ban Chiếu dời đô.
