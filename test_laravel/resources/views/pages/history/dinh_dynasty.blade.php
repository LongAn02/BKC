            <p class="container-history">
                Nhà Đinh (chữ Nôm: 茹丁, chữ Hán: 丁朝, Hán Việt: Đinh triều) là triều đại quân chủ phong kiến trong lịch sử
                Việt Nam bắt đầu năm 968,
                sau khi Đinh Tiên Hoàng dẹp xong loạn 12 sứ quân thống nhất đất nước và kết thúc năm 980 khi con của
                Đinh Tiên Hoàng là Đinh Phế Đế nhường
                ngôi cho Lê Hoàn.

                Nhà Đinh là triều đại mở đầu chế độ phong kiến tập quyền và thời kỳ tự chủ với một chế độ đứng đầu bởi
                một Hoàng đế của nước Việt Nam.
                Vương triều nhà Đinh đã mở nền chính thống cho thời đại phong kiến độc lập cho nên trong các bộ chính sử
                kể từ Đại Việt sử ký toàn thư thế kỷ XV,
                Đại Việt sử ký tiền biên thế kỷ XVIII đến Khâm định Việt sử thông giám cương mục thế kỷ XIX thì triều
                đại này đều được các tác giả lấy
                làm mốc mở đầu phần Bản kỷ hoặc Chính biên. Tiếp đến, việc xưng đế hiệu Đại Thắng Minh Hoàng đế của Đinh
                Tiên Hoàng mang ý thức lớn về
                sự tự tôn của nước Việt, có hàm ý so sánh với các Hoàng đế Trung Hoa.
                <br>
                <b>Thống nhất giang sơn</b><br>
                Năm 944, Ngô Quyền (vua tự xưng là Ngô Vương) mất, anh/em vợ là Dương Tam Kha cướp ngôi của nhà Ngô,
                xưng Dương Bình Vương. Các nơi không chịu thuần phục, nhiều thủ lĩnh nổi lên cát cứ các vùng thường đem
                quân đánh lẫn nhau.
                Con trưởng của Ngô Quyền là Ngô Xương Ngập bỏ trốn. Dương Tam Kha nhận Ngô Xương Văn - con thứ của Ngô
                Quyền - làm con nuôi. Năm 950, Ngô Xương Văn lật đổ Dương Tam Kha, trở thành Nam Tấn Vương. Ngô Xương
                Ngập được đưa về, cũng làm vua, là Thiên Sách Vương. Đó là thời Hậu Ngô Vương.
                <br>
                Năm 954, Ngô Xương Ngập chết. Đến năm 965, Ngô Xương Văn chết, con Ngô Xương Ngập là Ngô Xương Xí nối
                nghiệp. Nhưng vì thế lực suy yếu nên lui về giữ đất Bình Kiều. Quý tộc nhà Ngô, các tướng nhà Ngô cùng
                các thủ lĩnh địa phương đều nổi dậy chiếm cứ một vùng. Bắt đầu từ đó hình thành thế cục mà sử sách gọi
                là loạn 12 sứ quân.

                Trong số các lực lượng nổi dậy chống triều đình, nổi lên Đinh Bộ Lĩnh (Đinh Hoàn). Ông là người Hoa Lư,
                châu Đại Hoàng. Có cha là Đinh Công Trứ - nha tướng của Dương Đình Nghệ, giữ chức Thứ sử Hoan Châu(Nghệ
                An, Hà Tĩnh ngày nay).

                Thời kỳ đó, Đinh Bộ Lĩnh đã cùng những người thân thiết tổ chức lực lượng, rèn vũ khí, xây dựng căn cứ ở
                Hoa Lư. Sau vì bất đồng với người chú, Đinh Bộ Lĩnh cùng con trai Đinh Liễn sang đầu quân trong đạo binh
                của Sứ quân Trần Minh Công (Trần Lãm) ở Bố Hải Khẩu. Sau khi Trần Minh Công chết,[3] Đinh Bộ Lĩnh thay
                quyền, chiêu mộ binh lính, chiêu dụ được sứ quân Phạm Bạch Hổ cùng nhiều sứ quân khác chống nhà Ngô và
                tiến đánh các sứ quân còn lại.

                Trong hơn 3 năm, nhờ tài năng của mình, Đinh Bộ Lĩnh được nhân dân nhiều địa phương giúp sức, ủng hộ,
                đánh đâu thắng đấy, được tôn là Vạn Thắng vương. Các sứ quân lần lượt bị đánh bại hoặc xin hàng. Tình
                trạng cát cứ chấm dứt. Cuối năm 967, đất nước trở lại bình yên, thống nhất.
                <br>
                <b>Đinh Tiên Hoàng</b><br>
                Năm 968, Đinh Bộ Lĩnh lên ngôi Hoàng đế, tự xưng là Đại Thắng Minh Hoàng đế, tức là Đinh Tiên Hoàng, đặt
                quốc hiệu là Đại Cồ Việt, đóng đô ở Hoa Lư.

                Đinh Bộ Lĩnh có ba con trai: Đinh Liễn, Đinh Toàn (Đinh Tuệ) và Đinh Hạng Lang. Đinh Liễn là con cả, đã
                cùng Đinh Bộ Lĩnh đánh dẹp 12 sứ quân. Năm 978, Đinh Bộ Lĩnh lập con út là Hạng Lang làm thái tử. Đinh
                Liễn quá tức giận nên giết chết Hạng Lang vào mùa xuân năm 979.

                Cuối năm 979, Đinh Tiên Hoàng và Đinh Liễn bị sát hại trong cung. Sử ghi thủ phạm là nội nhân Đỗ Thích,
                nhưng các nhà nghiên cứu gần đây đặt ra giả thiết khác, cho rằng chủ mưu là Lê Hoàn (sau này làm vua và
                gọi là Lê Đại Hành) và Dương hậu.<br>

                <b>Đinh Phế Đế</b><br>
                Năm 979 Đinh Toàn, con trai còn lại của Đinh Bộ Lĩnh lên ngôi, là Đinh Phế Đế.

                Đinh Toàn khi đó mới 6 tuổi. Quyền lực thực tế nằm trong tay Thập đạo tướng quân Lê Hoàn, là nhiếp
                chính. Các đại thần Đinh Điền, Nguyễn Bặc, Phạm Hạp thấy vậy, lại nghi Lê Hoàn tư thông cùng Thái hậu
                Dương Vân Nga nên cử binh đến đánh. Lê Hoàn dẹp tan, Đinh Điền, Nguyễn Bặc, Phạm Hạp đều bị giết chết.
                Phò mã Ngô Nhật Khánh bỏ trốn vào nam sang Chăm Pa, sau đó cùng vua Chăm Pa với hơn nghìn chiến thuyền
                định đến đánh kinh đô Hoa Lư nhưng bị bão chết.

                Năm 980, nhà Tống rục rịch điều quân sang đánh Đại Cồ Việt, Thái hậu Dương Vân Nga cùng triều thần tôn
                Lê Hoàn lên làm vua, tức vua Lê Đại Hành. Nhà Đinh kết thúc, truyền được đến đời thứ hai, trị vì 12 năm
                (968-980).

                Lê Hoàn sau khi lên ngôi lập ra nhà Tiền Lê đã đánh thắng quân Tống (tháng 4 năm 981). Đinh Toàn trở
                thành Vệ Vương có mặt trong triều đình Tiền Lê 20 năm. Năm 1001, trong lần cùng vua Lê Đại Hành đi dẹp
                loạn Cử Long thuộc vùng Cầm Thủy Thanh Hóa, Đinh Toàn bị trúng tên chết.<br>

                <b>Bộ máy chính quyền</b><br>
                Năm 971, Đinh Tiên Hoàng bắt đầu quy định cấp bậc văn võ trong triều đình. Trong triều có sự tham gia
                của các nhà sư vì những đóng góp của họ trong quá trình dẹp loạn 12 sứ quân. Vua Đinh phong cho các
                quan văn võ:

                Nguyễn Bặc làm Định quốc công
                Đinh Điền làm Ngoại giáp
                Lưu Cơ làm Đô hộ phủ sĩ sư (chức vụ coi việc hình án)
                Lê Hoàn làm Thập đạo tướng quân
                Tăng thống Ngô Chân Lưu được ban hiệu là Khuông Việt đại sư
                Trương Ma Ni làm Tăng lục
                Đạo sĩ Đặng Huyền Quang được trao chức Sùng chân uy nghi.
                Năm 975 vua Đinh ban quy định áo mũ cho các quan văn võ. Bộ máy chính quyền nhà Đinh vẫn được xem là
                còn đơn sơ.<br>

                <b>Quân đội</b><br>
                Theo sử sách, quân đội nhà Đinh có mười đạo: mỗi đạo có 10 quân, 1 quân 10 lữ, 1 lữ 10 tốt, 1 tốt 10
                ngũ, 1 ngũ 10 người. Như vậy tổng số theo lý thuyết là 1 triệu người.

                Tuy nhiên, trong các nhà nghiên cứu có nhiều ý kiến cho rằng con số đó không có thực. Chữ Thập, theo Lê
                Văn Siêu, chỉ mang tính khái quát toàn thể về cách tổ chức kiểu "ngụ binh ư nông" như nhà Lý sau này,
                thời bình cho làm ruộng, chỉ huy động khi cần; còn Trần Trọng Kim ước đoán quân đội nhà Đinh nhiều
                nhất chỉ có đến 10 vạn người.<br>

                <b>Pháp luật</b><br>
                Do ảnh hưởng nhiều năm từ thời loạn lạc, có nhiều người quen thói lúc loạn, không chịu tuân theo luật
                lệ. Do đó vua Tiên Hoàng dùng pháp luật nghiêm ngặt để trừng trị.

                Đại Việt sử ký toàn thư ghi về việc này:

                Vua [Đinh Tiên Hoàng] muốn dùng uy chế ngự thiên hạ, bèn đặt vạc lớn ở sân triều, nuôi hổ dữ trong cũi,
                hạ lệnh rằng: "Kẻ nào trái phép phải chịu tội bỏ vạc dầu, cho hổ ăn". Mọi người đều sợ phục, không ai
                dám phạm.
                Trần Trọng Kim cho rằng "hình uy nghiêm như thế, thì cũng quá lắm, nhưng nhờ có những hình luật ấy thì
                dân trong nước mới dần dần được yên".
